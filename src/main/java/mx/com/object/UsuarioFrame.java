    //evento de accion para el boton "Registrar usuario"
    private void btnRegistrarUsuario(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarUsuario
        if(verificarCampos()){//verifica que no esten vacios los campos
            UsuarioDAO usuarioDao = new UsuarioDAO();//objeto para usar los query de usuario
            Usuario usuario = new Usuario();//objeto de usuario

            //se establecen en el objeto los valores que ingresa el usuario
            usuario.setId(Integer.parseInt(txtId.getText()));
            usuario.setNombre(txtNombre.getText());
            usuario.setApellido(txtApellido.getText());
            usuario.setCuenta(txtCuenta.getText());
            usuario.setContrasena(txtContrasena.getText());
            usuario.setPuesto(txtPuesto.getText());
            if(usuarioDao.registro(usuario)){//se registran los datos del objeto
                //se informa que se registraron correctamente
                JOptionPane.showMessageDialog(null,"Se ha registrado un nuevo usuario","Informacion",JOptionPane.INFORMATION_MESSAGE); //mensaje de guardado exitoso
                limpiar();//se limpian los campos
            }
        }else{//de no registrarse correctamente
            //se le informa al usuario
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos");
        }   
    }//GEN-LAST:event_btnRegistrarUsuario

    //metodo para verificar que no esten vacios los campos
    private boolean verificarCampos(){
        boolean band = true;
        
        if(txtId.getText().isEmpty() && 
           txtNombre.getText().isEmpty() && 
           txtApellido.getText().isEmpty() && 
           txtCuenta.getText().isEmpty() &&
           txtContrasena.getText().isEmpty() &&
           txtPuesto.getText().isEmpty()){
                band = false;
        }
        return band;
    }
    
    //metodo para limpiar los campos
    private void limpiar(){
        txtId.setText("");
        txtNombre.setText("");
        txtApellido.setText("");
        txtCuenta.setText("");
        txtContrasena.setText("");
        txtPuesto.setText("");
    }
